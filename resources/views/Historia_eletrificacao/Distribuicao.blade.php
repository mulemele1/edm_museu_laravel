            <div class="uk-width-expand@m uk-width-1-1@m uk-margin-remove">
                <h3 class="uk-heading-bullet uk-text-bolder uk-text-primary">História da Distribuição
                </h3>

                <p class="uk-dropcap uk-text uk-margin-left uk-margin-right uk-text-justify">A Distribuição de
                    energia eléctrica foi desde as primeiras fases o lado visível do Fornecimento de corrente
                    eléctrica,
                    precisamente porque com recurso a um simples gerador em funcionamento, e interligado a uma
                    rede levava a corrente as
                    residências localizadas numa área pré-definida, inclusive iluminava as ruas dos concelhos,
                    escolas missionárias e locais
                    que não tinham acesso ao serviço público de Fornecimento de corrente eléctrica. Era aquele
                    modelo de distribuição que a
                    maior parte das pessoas entrava em contacto com a electricidade nas zonas rurais e áreas
                    remotas.
                </p>

                <p class="uk-text uk-margin-left uk-margin-right uk-text-justify">Nas cidades e vilas onde o
                    serviço público era prestado havia uma rede estruturada, com postes delineados que
                    distribuíam a corrente até ao consumidor final. Em Lourenço Marques em particular, no
                    período que medeia entre 1903-1946
                    e de 1947-1963, a distribuição era feita com recurso a duas modalidades: Havia uma rede
                    constituída de postes de aço de
                    10,5 metros, condutores de cobre de maior secção, normalmente 200 mm2 ou mais,
                    preferencialmente em cabo vulcanizado que
                    levavam a corrente de 500V para os circuitos, que eram instalados em locais definidos pela
                    entidade fornecedora. Os
                    circuitos funcionavam como se fossem Postos de Transformação na actualidade, quer dizer,
                    recebiam em 500 V e lançava na
                    rede secundária uma tensão regulada, provavelmente 110V, isto na fase da corrente contínua.
                    A opção dos 500 V e a maior
                    secção procurava evitar quedas de tensão ao longo do percurso até aos circuitos. Do circuito
                    partiam linhas médias com
                    postes de 8,5 metros de altura, condutores de cobre que seguiam ao consumidor final,
                    tecnologia aquela que vincou até ao
                    advento da Nova Rede de Distribuição em 1964, com o funcionamento da Central Térmica, que
                    fora adjudicada a SONEFE, que
                    correlativamente rompeu o anterior sistema dando lugar a corrente alternada, que perdurou a
                    actualidade.</p>

                <p class="uk-text uk-margin-left uk-margin-right uk-text-justify">O circuito nr 01 estava
                    localizado ao longo da Karl Marx, próximo da Rua Araújo na Baixa. O nr 02 estava na área do
                    actual Banco de Moçambique e o 3 estava entre Avenida Samora Machel e 25 de Setembro,
                    enquanto o 4 situava-se na zona do
                    actual BCI na 25 de Setembro. O circuito 2-B estava no extremo leste da Avenida Ahmed Sekou
                    Touré.
                </p>

                <p class="uk-text uk-margin-left uk-margin-right uk-text-justify">Com a extinção daquela rede de
                    distribuição emergiu a rede subterrânea na parte urbana, enquanto nos bairros permaneceu
                    a rede aérea, e consequentemente foram reaproveitados os postes que ao longo do tempo haviam
                    sustentado o serviço
                    público de fornecimento de electricidade para assegurar aquelas zonas. Porém, na cidade da
                    Matola a distribuição de
                    energia eléctrica iniciou em 1962, estreitamente entrelaçada a rede aérea, corrente
                    alternada, não tendo tido a
                    experiência de Maputo.</p>

                <p
                    class="uk-text uk-text-italic uk-margin-left uk-margin-right uk-text-justify uk-margin-small-top">
                    Em Gaza, o estabelecimento da Rede de distribuição foi feito em 1901. Paradoxalmente,
                    dificuldades de vária ordem
                    contrariavam a evolução. Pela portaria 14.567 de 18 de Março de 1960, a Camara Municipal
                    solicitou o crédito bancário de
                    6.650.000 escudos no Banco de Fomento Nacional para remodelação da Central e a Rede de
                    distribuição do Município de
                    Gaza. O mesmo aconteceu com Inhambane que pela portaria 14.501 de 3 de Dezembro do mesmo
                    ano, solicitou 10.000 contos na
                    Caixa Económica postal para construção da Rede de Distribuição e diversos maquinismos para
                    central.</p>
                <p
                    class="uk-text uk-text-italic uk-margin-left uk-margin-right uk-text-justify uk-margin-small-top">
                    Em resumo: A Rede de distribuição das cidades ao longo do país era construída com recurso a
                    créditos bancários,
                    avaliando as receitas da empresa municipal, expectativas de reembolso nos anos subsequentes,
                    oque a partida constituía
                    revés no processo de ampliação daquelas infra-estruturas, tomando como base que Maputo
                    necessitou de 120.000 contos para
                    instalação da respectiva rede.</p>
                <p
                    class="uk-text uk-text-italic uk-margin-left uk-margin-right uk-text-justify uk-margin-small-top">
                    Com os fundos disponibilizados a camara através do sector de urbanização, entidade vinculada
                    ao Município fazia postes
                    de betão, adquiria os condutores e posteriormente instalava a rede de distribuição, facto
                    que conduziu para que a
                    energia eléctrica estivesse circunscrita a cidade cimento, raramente aos bairros
                    circunvizinhos. </p>
                <p
                    class="uk-text uk-text-italic uk-margin-left uk-margin-right uk-text-justify uk-margin-small-top">
                    A partir dos anos de 1973 e 1974, assiste-se o desabrochar de uma nova onda tendente ampliar
                    as centrais eléctricas das
                    Vilas de menor dimensão, consequentemente a edificação de linhas de distribuição para
                    sectores com impacto na vida das
                    urbes, que correlativamente beneficiaram os bairros, projectos que viriam a ser terminados
                    no pós-independência, tendo
                    como exemplo a cidade de Quelimane que perante dificuldades, o presidente da camara
                    deslocou-se a Maputo para solicitar
                    apoio dos SMAE de formas que fossem enviados técnicos para diagnosticarem os problemas.
                </p>

                <p class="uk-text uk-margin-left uk-margin-right uk-text-justify">Na altura que se criou a
                    Electricidade de Moçambique, o fornecimento de energia eléctrica era assegurado pelas
                    Camaras
                    Municipais espalhadas pelo país, constatando-se uma diversidade de redes, equipamentos com
                    diferentes realidades,
                    rotinas de trabalho de exploração. Para corrigir as adversidades a Direcção de Distribuição,
                    entidade criada para
                    regular a actividade encarregou-se na uniformização da concepção das redes, níveis de
                    tensão, rotinas de conservação e
                    construção. Diante da realidade foram estabelecidos os níveis de 11 e 33 KV como padrões,
                    apesar de existirem redes de
                    6,6 e 22 KV, assumindo que a rede de 6,6 estava na Katembe e Chimoio enquanto a de 22 KV
                    encontrava-se em
                    Ressano-Garcia, Beira e Chimoio, situação que pressupunha remodelação e adopção dos novos
                    performance (Revista dos 10
                    anos da EDM).
                </p>

                <p
                    class="uk-text uk-margin-left uk-margin-small-topuk-margin-right uk-text-justify uk-margin-small-top">
                    Em 1980 foi feito um levantamento das famílias que beneficiavam de corrente eléctrica nas
                    zonas rurais, tendo constatado
                    que apenas 30.323 tinham acesso aqueles serviços, o que representava 1,3% do total das
                    famílias que habitavam nas zonas
                    rurais na época (Revista dos 10 anos da EDM).</p>


