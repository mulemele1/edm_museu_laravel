            <p class="uk-text uk-margin-left uk-margin-right uk-text-justify">Foi nessa altura que foram
                desenhadas estratégias no
                contexto de proceder-se expansão das redes de distribuição junto
                dos principais rios nas províncias de Maputo, Gaza, Manica, Sofala, Tete, Zambézia e Nampula o
                que implicava um
                grande
                esforço de natureza financeira, material e humana.</p>

            <p class="uk-text uk-margin-left uk-margin-small-top uk-margin-right uk-text-justify">Em Maputo,
                concretamente Marracuene e Manhiça, foram
                construídos ramais de MT a partir da Linha de 30 KV que seguia a
                Manhiça para alimentar os agricultores e camponeses localizados ao longo do Incomáti, que
                através de electrobombas e
                diques irrigavam os campos, enchiam-nos para as fases que o rio estivesse a níveis
                incomportáveis, acções que
                executavam
                com apoio da corrente eléctrica. Contraditoriamente, quando o projecto começou a produzir
                resultados uma onda de
                desinformação circulou a volta da área onde se implementava. O primeiro alvo foram os PTS
                instalados, que passaram a
                ser
                vandalizados oque condicionou a remoção de um único PTS, posteriormente foi montado em Bobole
                passando alimentar aos
                residentes. Embora com dimensões diferentes, outras regiões que haviam beneficiado de redes de
                distribuição para
                incrementar a produção agrícola passaram por cenários perversos (Entrevista com Inácio Machava,
                reformado da EDM em
                Marracuene).
            </p>

            <p class="uk-text uk-margin-left uk-margin-small-top uk-margin-right uk-text-justify">A EDM tinha
                herdado a rede de distribuição constituída
                por cobre, que associava-se aos condutores de PBT e outros
                materiais pouco recomendados, instalados por particulares nos bairros. Existiam naquele processo
                postes de ferro que
                geravam uma miscelânea, aumentando as quedas de tensão. Para colmatar adquiriu condutores de
                alumínio como primeiro
                ensaio, e passou aplicar, cujo resultado não foi satisfatório, por causa dos cruzamentos de
                linha, menor segurança
                na
                operação da rede, oque resultou em diversos estudos.</p>


            <p class="uk-text uk-margin-left uk-margin-remove-top uk-margin-right uk-text-justify">Com as cheias
                de 2000 a rede da Matola e a parte suburbana de Maputo ficou submersa. Houve cortes excessivos
                das linhas
                de BT, queda de postes, dentre outras anomalias. Foi na mesma altura que descobriu-se que os
                perfis anteriores das
                linhas de distribuição não se ajustavam a realidade, pois algumas passavam em áreas susceptíveis
                a inundações, pântanos,
                pequenos charcos etc.</p>

            <p class="uk-text uk-margin-left uk-margin-small-top uk-margin-right uk-text-justify">
                Coincidentemente, a empresa havia iniciado a implementar o cabo torçado desde 1999, e ao retomar
                a reconstrução das
                linhas danificadas teve que utilizar aquele material, substituindo os postes de ferro pelos de
                madeira, dentre outros
                pormenores, situação que fez nascer nova Rede de distribuição em todos bairros das duas cidades
                (Entrevista com Abel
                Mulhovo e Carlos Cossa da Matola).</p>

            <p class="uk-text uk-margin-left uk-margin-small-top uk-margin-right uk-text-justify">Paralelamente,
                as Áreas Operacionais e ZD’s vinham implementando aquele material na execução dos trabalhos. Com
                o
                projecto de Emergência e a implementação de projectos de Transporte no âmbito da electrificação
                às capitais provinciais
                contemplava subprojectos de Distribuição, que reabilitava toda cidade e os bairros,
                posteriormente avançava para os
                distritos e povoados oque permitiu a remoção dos condutores de cobre, e assumir o cabo torçado
                como marca que a empresa
                adoptou no território nacional.</p>
