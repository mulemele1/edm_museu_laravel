                                disso,
                                com a evolução da ponta na rede da EDM, sobretudo no periodo nocturno, tem sido utilizados
                                alguns grupos para minorar o
                                problema, dali que emergiram grupos de emergencia que produzem corrente electrica nos dias
                                de indisponibilidade ou horas
                                de maior consumo para compensar.
