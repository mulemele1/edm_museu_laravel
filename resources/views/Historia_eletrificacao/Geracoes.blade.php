                </h3>
    
                <p class="uk-dropcap uk-text uk-margin-left uk-margin-small-top  uk-text-justify">O Fornecimento de energia
                    eléctrica em Moçambique esteve, inicialmente, vinculado as Camaras Municipais que adjudicava
                    entidades privadas para prestarem serviço devido ao fraco poder económico do Governo colonial português
                    que não dispunha
                    de recursos financeiros ao ponto de criar uma empresa capaz de exercer aquela função em todo território
                    nacional. Aliado
                    ao facto, a situação macroeconómica de princípios do século XX não era favorável a Portugal,
                    consequentemente o sector
                    eléctrico permaneceu estagnado, cabendo ao Estado o papel de regulador, facto que reduziu as
                    potencialidades da
                    actividade, concorrendo para que a electricidade estivesse circunscrita as cidades, que tinham
                    empresários capazes de
                    exercer a função, situação contrariada nas vilas e concelhos que não dispunham de corrente eléctrica.
                </p>
    
                <p class="uk-text uk-margin-left uk-margin-small-top  uk-text-justify">Foi assim que a Central de Lourenço
                    Marques foi adjudicada a Joost Gulber co, posteriormente à Compagne D’Electricite de
                    Lourenço Marques, e mais tarde a Delagoa Bay. Em Tete, a Central esteve adjudicada ao empresário José
                    Serras Pires.
                    Aliado ao fenómeno, a energia eléctrica não chegava a muitas vilas e Concelhos. Para contornar, em 1964,
                    a Direcção dos
                    Serviços de Administração Civil emitiu uma circular, transcrevendo o ofício da Repartição dos Serviços
                    Eléctricos do
                    Ministério do Ultramar, que instruía que fosse estimulada a Criação de Serviços de Electricidade, por
                    Federação de
                    Municípios, para distribuição de energia eléctrica (Acta dos SMAE 12/64, de 23 de Março).
                </p>
    
                <p class="uk-text uk-margin-left uk-margin-small-top  uk-text-justify">A partir daquele dispositivo
                    normativo e outros elaborados a posterior algumas vilas passaram a dispor de centrais
                    eléctricas com as respectivas maquinarias, redes de distribuição e oportunidades de concessão de
                    créditos bancários para
                    dar seguimento as actividades, operando sob a chancela de Juntas Locais. </p>
    
                <p class="uk-text uk-margin-left uk-margin-small-top  uk-text-justify">A partir de 1947 a situação económica
                    de Portugal começou a melhorar por intermédio da Ajuda Americana à Europa, que
                    subsequentemente gerou os Planos de Fomento I e II, com objectivo de restabelecer a economia devastada
                    no velho
                    continente, fruto da II Guerra Mundial, embora obviamente Portugal não tivesse entrado no conflito viria
                    a beneficiar
                    daqueles fundos, e aplicou-os nos projectos de interesse económico, incluindo nas colónias.</p>
    
                <p class="uk-text uk-margin-left uk-margin-small-top  uk-text-justify">Foi no contexto dos recursos
                    disponíveis que emergiu um movimento tendente a dinamizar a economia, com a implementação
                    de diversos projectos que consequentemente requeriam de corrente eléctrica, facto que concorreu para
                    expropriação das
                    concessionárias e posterior criação dos Serviços Municipalizados de Água e Electricidade (S.M.A.E), que
                    viria a ser
                    instituído em Maputo em 27 de Outubro de 1947. No mesmo pensamento, foi constituída a Sociedade
                    Hidroeléctrica do Revué
                    (SHER), através da portaria 35.744 de 24 de Agosto de 1946, que no fundo respondia o pedido da Sociedade
                    Algodoeira de
                    Fomento colonial, sem descurar a Sociedade Nacional de Estudos e Financiamentos Ultramarinos (SONEFE), e
                    Serviços
                    Autónomos de Electricidade (SAE).
                </p>
    
                <p class="uk-text uk-margin-left uk-margin-small-top  uk-text-justify">Deste modo, em finais de 1947 o
