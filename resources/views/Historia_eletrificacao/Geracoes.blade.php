                    Serviço Municipalizado de Água e Electricidade em Xai-Xai, Inhambane, Quelimane, Nampula, Pemba e
                    Lichinga, enquanto
                    Chokwe,Tete e Nacala remeteu sob gestão dos SAE. A SONEFE no âmbito do mesmo processo passou a ser
                    concessionária
                    centrada na produção e Transporte de corrente eléctrica, que correlativamente fornecia aos
                    distribuidores que eram os
                    SMAE e SAE.</p>
    
                <p class="uk-text uk-margin-left uk-margin-small-top  uk-text-justify">Em Maputo, o Fornecimento de energia
                    eléctrica começou com a Joost &Gulber co, em finais de Outubro de 1895, que havia
