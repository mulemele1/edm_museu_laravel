                                anos
                                de 1982 a 1988, com a capacidade de 0,6 MW e 1MW respectivamente, no contexto de mitigar o
                                problema candente atravessado
                                na época.
                                Em Moamba no Maputo, junto ao rio Incomati foi construida a central Hidroelectrica de
                                Corumana, com a potência de 16MW,
                                nos anos de 1983-1988.
                            </p>
    
                            <p class="uk-text uk-margin-left uk-margin-small-top  uk-text-justify">Com o estabelecimento da
