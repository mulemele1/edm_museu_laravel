                <div class="uk-margin-right">
                    <div class="uk-text-center" uk-grid>
                        <div class="uk-width-expand@m">
                            <p class="uk-text uk-margin-left uk-margin-small-top  uk-text-justify">As cidades de Xai-Xai,
                                Chokwe, Inhambane, Quelimane, Tete, Nampula, Nacala, Pemba e Lichinga possuiam desde o tempo
                                colonial centrais electricas, geridas pelos respectivos Municipios, que no fundo eram
                                sistemas isolados, sem
                                interligação com um centro produtor de referência capaz de disponibilizar a corrente na rede
                                daquelas cidades ou vilas.
                                Este facto, trouxe constrangimentos ao longo do tempo, devido aos altos custos de manutenção
                                dos grupos geradores,
                                combustiveis dentre outros, facto que prevaleceu até a criação da EDM em 27 de Agosto de
                                1977.
    
                            </p>
                            <p class="uk-text uk-margin-left uk-margin-small-top  uk-text-justify">A EDM na primeira
                                estrutura da Empresa criou a Direcção de Engenharia de Centrais, que era a entidade
                                vocacionada pelas
                                centrais térmicas, hidroelectricas etc. Foi a mesma em parceria com outras direcções que
                                desencadeou o levantamento da
                                maquinária existente ao nivel da empresa e concluiu que os grupos existentes nas centrais
                                espalhadas pelo territorio
                                nacional eram de marcas e fabricantes diferentes; não existiam peças sobressalentes, a
                                maioria dos operadores não tinha
                                formação adequada de acordo a função, dentre outras anomalias(Revista dos 10 anos da EDM).
    
                            </p>
    
    
                            <p class="uk-text uk-margin-left uk-margin-small-top  uk-text-justify">Para contornar, a Empresa
                                em parceria com os organismos internacionais (PNUD) e parceiros, juntaram esforços convista
                                aquisição de grupos para as centrais, Reabilitação das instalações e formação dos
                                operadores, que permitiram potenciar
                                as infraestruturas, esperançosos pelos projectos de Transporte que viriam a posterior. Foi
                                naquele contexto que foi
                                construida a central de Nacala com a capacidade de 24 MW que por intermedio da linha de 110
                                KV passou alimentar Nampula;
                                foi igualmente reabilitada e montados novos grupos nas centrais de Nampula, Quelimane,
                                Mocuba, Xai-Xai, inhambane, Pemba
                                e Lichinga, dentre outras.
                            </p>
    
