            <div class="uk-width-expand@m uk-width-1-1@m uk-margin-remove">
                <h3 class="uk-heading-bullet uk-text-bolder uk-text-primary">História de Transporte
                </h3>

                <p class="uk-dropcap uk-text uk-margin-left uk-margin-remove-right uk-text-justify">A primeira
                    linha de transporte na história do país surgiu no período colonial com a Sociedade
                    Hidroeléctrica do Revué,
                    como resultado de efeitos conjugados entre a SHER e o Município da Beira que após
                    negociações no sentido de fornecer
                    corrente eléctrica em Alta Tensão àquela urbe a edilidade decidiu contrair um empréstimo no
                    Banco Nacional Ultramarino
                    na ordem de 18.500 contos que foram adicionados aos fundos da SHER na perspectiva de
                    viabilizar a construção da linha de
                    Transporte conhecida na Literatura por Beira1, que viria ser executada pela "Société Anonima
                    de Electrificazione" de
                    Milão, vulgo SAE. Foi no mesmo processo que foi instalada a subestação de 110 KV na Munhava
                    ao lado da antiga central,
                    que foi concluída em Janeiro de 1956. A linha Beira1 ao longo do percurso alimentava as
                    Subestações de Lamego e
                    Mafambisse.
                </p>

                <p class="uk-text uk-margin-left uk-margin-remove-right uk-text-justify">Com a evolução
                    progressiva dos consumos na Beira, em 1971 a SHER adjudicou ACTA, Actividades Eléctricas
                    Associadas a
                    construção da 2ª linha de Transporte, desta feita para a subestação de Dondo e finalmente a
                    Munhava, no contexto de
                    reforçar o fornecimento, reconhecendo que a SHER alimentava o Porto, a fábrica de cimentos e
                    um conjunto de indústrias
                    de transformação localizadas no eixo Dondo-Beira para além dos Serviços Municipalizados de
                    Electricidade da Beira.
                    Igualmente, a linha Beira-2 era de 110KV, cuja finalização das obras ocorreu em finais de
                    1972. </p>

                <p class="uk-text uk-margin-left uk-margin-remove-right uk-text-justify">No mesmo pensamento, a
                    SONEFE em Maputo, após o início da Produção de corrente eléctrica na Central Térmica,
                    constatou
                    que para satisfazer a demanda necessitária de instalar mais grupos para além dos previstos.
                    Para reverter o cenário, em
                    1960 equacionava fazer o aproveitamento hidroeléctrico de Massingir em Gaza, como mecanismo
                    de evitar a instalação de
                    mais grupos geradores na Central, que iria fornecer 40MVA e transportar a 150KV até Maputo.
                    No entanto, duma vez que
                    aquela possibilidade não estava disponível a outra alternativa era importar da rede da ESCOM
                    na Africa do sul que se
                    encontrava a cerca de 80 kms de Maputo (Memória Descritiva para Construção da Linha de 275KV
                    da Africa do Sul para
                    Infulene- pág. 7).
                </p>

                <p class="uk-text uk-margin-left uk-margin-small-top uk-margin-remove-right uk-text-justify">
                    Para dar início ao processo de importação, a SONEFE desenvolveu estudos de campo onde
                    identificou ao longo do trajecto
                    constrangimentos mitigáveis: rios, povoados e pontos de travessia de linha férrea, ao mesmo
                    tempo que encarregou a ESCOM
                    para desencadear a topografia no lado sul-africano, seguidamente lançou concursos para o
                    extremo da RSA e outro para o
                    lado moçambicano.</p>

                <p class="uk-text uk-margin-left uk-margin-small-top uk-margin-remove-right uk-text-justify">O
                    concurso em Moçambique era relativo a construção da linha de 275KV de Ressano-Garcia a
                    Infulene incluindo a
                    subestação, numa distância de 77 kms, para além de duas linhas de 60 KV do Infulene para
                    Central Térmica, enquanto do
                    extremo sul-africano a empreitada estava centrada na construção da linha de transmissão da
                    subestação de Distribuição de
                    Perseus em Vicinity Dalesville para a subestação de Distribuição de Kimberley, que estaria
                    em paralelo com a linha de
                    132 KV, numa distância de 59.6 milhas, cerca 96kms. Partindo da ideia que uma milha equivale
                    a 1.609 metros,
                    multiplicado por 59,6 milhas corresponde a 95.896 metros que convertido em kms equivalia
                    aproximadamente 96 kms.</p>

                <p class="uk-text uk-margin-left uk-margin-small-top uk-margin-remove-right uk-text-justify">A
                    Empresa que ganhou o concurso para construção da linha de Transporte e a subestação foi
                    ACTA, Actividades Eléctricas
                    Associadas SARL. Foi aquela que montou as torres, fundações e de mais trabalhos relativos a
                    empreitada incluindo a
                    subestação. Paralelamente, edificou as linhas de 60 KV do Infulene a Zona "D", que era o
                    local da subestação da Central
                    Térmica. No extremo sul-africano foi a própria ESCOM que executou os trabalhos. </p>

                <p class="uk-text uk-margin-left uk-margin-small-top uk-margin-remove-right uk-text-justify">Os
                    testes na subestação do Infulene com vista aferir a qualidade de tensão recebida foram
                    realizados nos dias 21 a 27 de
                    Novembro de 1972, na presença do Eng. Chefe da Fiscalização, Luís Botão da SONEFE e Daciano
                    de Barro da ACTA. Foi nos
                    ensaios que se apurou que a energia eléctrica ao invés dos previstos 275KV variava entre 300
                    a 320 KV. Apesar daqueles
                    constrangimentos, com efeitos a 25 de Novembro daquele ano, a subestação passou a operar
                    definitivamente.</p>

                <p class="uk-text uk-margin-left uk-margin-small-top uk-margin-remove-right uk-text-justify">No
                    fundo eram aqueles dois sistemas de interligação (SHER e SONEFE) que existiam até altura que
                    foi criada a EDM em
                    1977. As restantes cidades e vilas utilizavam sistemas isolados por intermédio de centrais
                    eléctricas que dispunham de
                    grupos geradores que dedicavam-se a produção e distribuíam aos consumidores, sem
                    possibilidade de receber de um centro
                    produtor. Apesar do paradoxo, em Nacala foi construída uma Central de 24 MW em 1980. A EDM
                    perante os problemas de
                    avarias constantes em Nampula construiu uma linha de 110 KV de Nacala a Nampula, que passou
                    a fornecer a corrente aquela
                    cidade. Pode-se atrever a dizer que aquela foi a primeira linha de Transporte que a EDM
                    construiu.</p>
