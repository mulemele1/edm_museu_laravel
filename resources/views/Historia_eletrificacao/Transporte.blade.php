            <p class="uk-text uk-margin-left uk-margin-small-top  uk-margin-remove-right uk-text-justify">Para
                dinamizar a electrificação do país, a EDM contou com a Hidroeléctrica de Cahora Bassa, que vinha
                sendo construída a
                partir de 1969, cujo término ocorreu em 1976, seguido da instalação da Subestação em Matambo a
                tensão de 220 KV em Tete.
                Foi mediante a existência daquelas infra-estruturas que iniciou a campanha de angariação de
                fundos com vista a
                construção de Linhas de Transmissão para fornecer as diversas regiões do país.</p>

            <p class="uk-text uk-margin-left uk-margin-small-top  uk-margin-remove-right uk-text-justify">No
                mesmo âmbito, a EDM negociou fundos dos Bancos italianos e Franceses para construção da Linha
                Centro-Norte que
                associou com os da suécia na 2ª fase da execução, tomando como referência que as cidades de
                Quelimane, Mocuba e Nampula
                enfrentavam um défice energético que necessitava ser colmatado, aliado aos altos consumos de
                combustíveis na central de
                Nacala. Foi assim que entre 1980-1986 foi executado o Projecto da Linha Centro-Norte, partindo
                de
                Matambo-Chimuara-Nicoadala-Quelimane-Mocuba-Alto-Molocue-Nampula em 220 KV. Uma vez chegada em
                Nampula, foi interligada
                a cidade de Nacala através da linha de 110KV, que por sinal era a mesma que fornecia Nampula
                antes daquele projecto.
                Adjudicatária das linhas foi a SAE italiana, enquanto para as subestações foi a Cogelex e a Swed
                Power foi consultora.</p>

            <p
                class="uk-text-italic uk-text uk-margin-left uk-margin-remove-right uk-text-justify uk-margin-small-top">
                Para potenciar as provincias de Manica e Sofala, no decurso da execução das obras da HCB a SHER
                havia negociado a
                construção da linha de 220 KV da HCB a Chibata, eventualmente num processo de parceria, que por
                sinal foram executadas.O
                local previsto de ser instalada a subestação da HCB no Chimoio era no Chibata e dali iria escoar
                a Chimoio e Beira. Com
                a Criação da EDM, foi reformulado o projecto no sentido de ser construida a subestação de
                Chigodora que receberia de
                Chibata para Chigodora e posteriormente aos diferentes receptores, facto que veio a ser
                inviabilizado devido a Guerra.
                Apesar dos consrangimenos, nos meados da decada 80 perante as intensas sabotagens da linha
                Beira1 e 2 a EDM decidiu
                reactivar a linha onde passou a utilizar em 110 KV contra os 220 previstos, com a subestação em
                Chibata. Foi naquele
                ambito que emergiu a linha para Nhamatanda que passava ao longo da estrada Nacional n 06,
                contrariamente das anteriores
                que passavam ao longo da floresta densa. Em Nhamatanda nasceu o P.S no ambio do mesmo processo
                que passou a fazer gestão
                das 3 linhas e dali as duas conduziam a Beira.</p>

            <p class="uk-text uk-margin-left uk-margin-top  uk-margin-remove-right uk-text-justify">Enquanto
                decorriam os trabalhos no extremo centro- norte, a EDM conseguiu financiamento dos Bancos
                Italianos e da ASDI,
                com objectivo de construir a Linha de Transmissão para o vale do Limpopo, partindo de
                Infulene-Macia-Chokwe-Xai-Xai, a
                uma tensão de 110KV. A execução foi efectuada nos anos de 1983-1984. Foi no mesmo contexto que
                efectivou-se a montagem
                de um Posto de seccionamento na Macia, que passou alimentar os consumidores naquela vila e as
                subestações de Chokwe e
                Xai-Xai, cujo terminó foi em Setembro de 1984. O projecto teve como gestores os Engs. Higino
                Fabião e Cesar Manuel. O
                Empreiteiro da Linha foi a SADELMI <i>Italiana</i>.</p>

            <p class="uk-text uk-margin-left uk-margin-top uk-margin-remove-right uk-text-justify">Com
                assinatura do Acordo Geral de Paz, que pôs fim a Guerra dos 16 anos em Moçambique, a EDM retomou
                o processo de
                construção de Linhas de Transporte, com fundos da Dinamarca onde foi possivel dar continuidade a
                linha de 110KV de
                Xai-Xai a Lindela, projecto que iniciou em finais de 1999 e terminou em 2004. Com o fim da
                execução daquela obra, as
                cidades de Inhambane e Maxixe passaram a beneficiar, pondo fim a dependência dos grupos
                geradores a que estavam votadas.</p>

            <p class="uk-text uk-margin-left uk-margin-top uk-margin-remove-right uk-text-justify">Para
                satisfazer as cidades de Gurué, Cuamba e Lichinga foi desenhado o Projecto com o mesmo nome que
                serviu de espinha
                dorsal para alimentar aquelas regiões e zonas circunvizinhas que não tinham acesso a corrente
                electrica. Naquela óptica,
                a linha de 110 KV partiu da subestação de Alto Molocué para Gurué onde passou alimentar aquela
                vila, Cuamba e finalmente
                Lichinga. O projecto teve financiamento da ASDI e outros parceiros. Adjudicataria foi ABB, que
                realizou os trabalhos
                entre 2002-2005.</p>

            <p class="uk-text uk-margin-left uk-margin-top uk-margin-remove-right uk-text-justify">Entre 2003 a
                2004, foi construida a linha Nampula-Pemba de 110 KV que estava inserida no prosseguimento da
                electrificação. Com a conclusao daquela foi implementada a linha Pemba-Macomia-Auasse também em
                110KV. Em 2017-2019 foi
                interligado Cuamba-Marrupa, também a uma tensão de 110KV e a linha Chibata-Dondo em 220 KV.</p>

            <p class="uk-text uk-margin-left uk-margin-top uk-margin-remove-right uk-text-justify">O Sector de
                Transporte era tutelado na Primeira estrutura da Empresa pelo Projecto da Linha Centro-Norte,
                criado em
                1980, cuja sede era na Filipe Samuel Magaia. O mesmo tinha o sector de protecções dirigido pelo
                Eng. Augusto de Sousa
                Fernando. Para complementar as actividades do Transporte existia o sector de Manutenção pesada,
                onde estava o consultor
                sueco Knut Kratzer, Cesar Manuel e Mário Jonas.No mesmo cenário, em 1997 foi afecto Eng. Joaquim
                Ou-Chim, actual
                Administrador de Electrificação e Projectos, que por diversas razões não ficou muito tempo tendo
                sido encaminhado para
                os Projectos, enquanto as Comunicações com recurso a ondas portadoras e Rádio era prosseguida
                pelo LETEC,onde estavam
                afectos Eng. Barronet,o noruegues Runner e outros. Era aquela estrutura que exercia os trabalhos
                relacionados ao
                Transporte ao nivel central. Paralelamente, em Infulene, e Chimoio tinha pessoal que dava
                seguimento as actividades. Com
                a extinção do Projecto da Linha Centro-Norte foi criada a Direcção de Engenharia de Redes.</p>

            <p class="uk-text uk-margin-left uk-margin-top uk-margin-remove-right uk-text-justify">Perante o
                paradoxo, a EDM em 1981 recrutou jovens estudantes, colocou no Centro Provisório de Formação na
                Eduardo
                Mondlane, que viria evoluir para departamento de Formação e Academia. Foi com aquele pessoal que
                arrancou o curso de
                subestações, contando que em 1983 as infraestruturas contempladas na fase 1 do projecto da Linha
                Centro-Norte estariam
                operacionais e precisariam de operadores, técnicos de protecções etc.</p>

            <p class="uk-text uk-margin-left uk-margin-top uk-margin-remove-right uk-text-justify">Findo o curso
                foram encaminhados para Quelimane e Mocuba para as prática até altura que as subestacões
                passaram a
                operar. Com a conclusao da linha de Transporte viriam a ser afectos integralmente, oque fez
                nascer com o tempo um corpo
                técnico ligado ao transporte em Quelimane, Nampula, Chimoio e Infulene, com supervisão na Filipe
                Samuel Magaia. Os dois
                sectores evoluiram, agregando mais profissionais aliado a intensificação das formações e a
                expansão da Rede de
                Transporte pelo país, nasceram departamentos de Produção e Transporte em Infulene que velava
                pela região sul, em Chimoio
                que velava chimoio e Beira, em Quelimane que estava centrado no eixo Chimuara –alto-Molocue, e
                Nampula.</p>

            <p class="uk-text uk-margin-left uk-margin-top uk-margin-remove-right uk-text-justify">Naquelas
                condições os pressupostos para criação da Direccção da Rede de Transporte existiam, pelo que em
                02 de Setembro
                de 2002, atraves do despacho 025/CA/2002, foi criada a DRT, ao mesmo tempo que foram extintos os
                Departamento de
                Transporte da Área Operacional de Quelimane; Departamento de Produção e Transporte da Área
                Operacional de Nampula;
                Departamento de Produção e Transporte da A.O de Nacala, e posterior integração daqueles técnicos
                na Direcção da Rede de
                Transporte, cujo Director nomeado foi Eng. Augusto de Sousa Fernando.</p>

            <p class="uk-text uk-margin-left uk-margin-top uk-margin-remove-right uk-text-justify">Ao longo do
                tempo as Áreas de Transporte passaram a velar a Rede no contexto regional passando a Direcção de
                Transporte
                Sul (DTSUL), DTCE, DTNO), enquanto a direcção central foi transformada para Direcção Geral de
                Transmissão e novamente
                DRT.</p>
